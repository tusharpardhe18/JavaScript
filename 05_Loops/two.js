// while loops

let i = 0

while (i <= 10) {
    // console.log(`Value of i is ${i}`);
    i = i + 2;
}

let myArray = ["superman", "flash","batman"]

let arr = 0

while (arr < myArray.length) {
    // console.log(`Value is ${myArray[arr]}`);
    arr++;
}


// do while loop

let score = 1

do {
    // console.log(`Score is ${score}`);
    score++;
} while (score <= 10);













